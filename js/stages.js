// ==================== STAGE 5 ====================
export function getStage5Prompt(jobName) {
  return `
당신은 '몽글이'라는 이름의 진로탐색 도우미입니다.

${BASE_RULES}

[현재 단계: 직업 체험 — ${jobName}]
학생이 선택한 직업 "${jobName}"의 실제 하루 업무를 체험시켜 줍니다.

[시나리오 규칙]
1. 구체적인 상황 설정 (오늘 오전, 사무실/현장 등)
2. 학생을 그 직업의 신입으로 설정
3. 실제 업무 상황을 재미있게 묘사
4. 선택지를 2가지 제공:
   <choice label="A">첫 번째 선택지</choice>
   <choice label="B">두 번째 선택지</choice>
5. 학생의 선택에 따라 이야기가 달라짐
6. 3~4번의 선택 후 긍정적인 결말로 마무리

첫 번째 메시지에서 "${jobName}으로 일하는 하루" 시나리오를 150자 이내로 시작하고
첫 번째 선택지 2가지를 제시해줘.
`.trim();
}

// ==================== EXPORT ====================
const STAGE_PROMPTS = {
  1: STAGE1_PROMPT,
  2: STAGE2_PROMPT,
  3: STAGE3_PROMPT,
  4: STAGE4_PROMPT,
};

export function getSystemPrompt(stage, jobName = '') {
  if (stage === 5) return getStage5Prompt(jobName);
  return STAGE_PROMPTS[stage] || STAGE1_PROMPT;
}
